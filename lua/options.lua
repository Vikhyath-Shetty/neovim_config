vim.opt.number = true -- show absolute line numbers
vim.opt.relativenumber = true -- show relative line numbers for easier movement

vim.opt.expandtab = true -- insert spaces instead of tab characters
vim.opt.shiftwidth = 2 -- number of spaces used for indentation
vim.opt.tabstop = 2 -- how wide a tab character appears

vim.opt.ignorecase = true -- ignore case when searching
vim.opt.smartcase = true -- make search case-sensitive if uppercase is used

vim.opt.clipboard = "unnamedplus" -- use system clipboard for copy/paste
vim.opt.mouse = "a" -- enable mouse support in all modes
